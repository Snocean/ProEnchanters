function GetNameAndID()
    local csvlist = {}
    for ench, t in pairs(CombinedEnchants) do
        local enchID = ench
        local enchantName = t.name
        local enchantSpellID = t.spell_id
        local enchSlot = t.slot
        local enchStats = t.stats
        local materials = ""
        local materialcount = 0
        local enchCn = EnchantLocales[enchID]["Chinese"]
        local enchEng = EnchantLocales[enchID]["English"]
        local enchFr = EnchantLocales[enchID]["French"]
        local enchGm = EnchantLocales[enchID]["German"]
        local enchKor = EnchantLocales[enchID]["Korean"]
        local enchMx = EnchantLocales[enchID]["Mexican"]
        local enchPor = EnchantLocales[enchID]["Portuguese"]
        local enchRus = EnchantLocales[enchID]["Russian"]
        local enchSpan = EnchantLocales[enchID]["Spanish"]
        local enchTai = EnchantLocales[enchID]["Taiwanese"]
        for i, t2 in pairs(t.materials) do
            if materialcount > 0 then
                materials = materials .. ", " .. t2
            else
                materials = t2
                materialcount = 1
            end
        end
        table.insert(csvlist, {enchID or "", enchantName or "", enchantSpellID or "", enchSlot or "", enchStats or "", materials or "", enchCn or "", enchFr or "", enchGm or "", enchKor or "", enchMx or "", enchPor or "", enchRus or "", enchSpan or "", enchTai or ""})
    end 
    for ind, t3 in ipairs(csvlist) do
        --print(t3[1] .. "^ " .. t3[2] .. "^ " .. t3[3] .. "^ " .. t3[4] .. "^ " .. t3[5] .. "^ " .. t3[6] .. "^ " .. t3[7] .. "^ " .. t3[8] .. "^ " .. t3[9] .. "^ " .. t3[10] .. "^ " .. t3[11] .. "^ " .. t3[12] .. "^ " .. t3[13] .. "^ " .. t3[14] .. "^ " .. t3[15])
    end
    return csvlist
end

-- Function to export table to CSV
local function exportToCSV(filename, data)
    -- Open the file in write mode
    local file = io.open(filename, "w")
    if not file then
        error("Could not open file for writing: " .. filename)
    end

    -- Write UTF-8 BOM (optional, ensures compatibility with some applications)
    file:write("\239\187\191")  -- UTF-8 BOM (Byte Order Mark)

    -- Write CSV header
    file:write("enchID^enchantName^enchantSpellID^enchSlot^enchStats^materials^enchCn^enchFr^enchGm^enchKor^enchMx^enchPor^enchRus^enchSpan^enchTai\n")

    -- Iterate over the table and write each row to the file
    for _, row in ipairs(data) do
        local csvLine = table.concat(row, "^")  -- Convert row to CSV format
        file:write(csvLine .. "\n")  -- Write the line to the file
    end

    -- Close the file
    file:close()
end

