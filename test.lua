-- Function to export table to CSV
local function exportToCSV(filename, data)
    -- Open the file in write mode
    local file = io.open(filename, "w")
    if not file then
        error("Could not open file for writing: " .. filename)
    end

    -- Write UTF-8 BOM (optional, ensures compatibility with some applications)
    file:write("\239\187\191")  -- UTF-8 BOM (Byte Order Mark)

    -- Write CSV header
    file:write("enchID^enchantName^enchantSpellID^enchSlot^enchStats^materials^enchCn^enchFr^enchGm^enchKor^enchMx^enchPor^enchRus^enchSpan^enchTai\n")

    -- Iterate over the table and write each row to the file
    for _, row in ipairs(data) do
        local csvLine = table.concat(row, "^")  -- Convert row to CSV format
        file:write(csvLine .. "\n")  -- Write the line to the file
    end

    -- Close the file
    file:close()
end

